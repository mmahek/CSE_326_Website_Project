import math
print("MIN VALUE:",min(8,3,5,678,456))
print("MAX VALUE",max(1,65,3,5,28,78))
print("POWER FUNCTION",pow(4,5))
print("ABSOLUTE VALUE",abs(-14.76))
print("ROUND-OFF VALUE",round(14.6))
print("ROUND-0FF VALUE",round(24.5))
print("floor value:",math.floor(58.7))    #give the value which is at left hand side
print("ceil value:",math.ceil(74.45))     #gives the value which is at right hand side
print("floor mod",math.fmod(10,3))        #basically gives remainder
print("trunced value:",math.trunc(12.4))

